///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Dependencies //////////////////////////////////////////////////////////////////////////////////////////////////////////
const {
   MessageButton
} = require('discord.js')
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Buttons ///////////////////////////////////////////////////////////////////////////////////////////////////////////////
const next = new MessageButton()
   .setLabel(`>`)
   .setStyle(`SECONDARY`)
   .setDisabled(false)
   .setCustomId(`nextbtn`);
const previous = new MessageButton()
   .setLabel(`<`)
   .setStyle(`SECONDARY`)
   .setDisabled(false)
   .setCustomId(`previousbtn`);
const first = new MessageButton()
   .setLabel(`<<`)
   .setStyle(`SECONDARY`)
   .setDisabled(false)
   .setCustomId(`firstbtn`);
const last = new MessageButton()
   .setLabel(`>>`)
   .setStyle(`SECONDARY`)
   .setDisabled(false)
   .setCustomId(`lastbtn`);
const del = new MessageButton()
   .setLabel(`ðŸ—‘`)
   .setStyle(`DANGER`)
   .setDisabled(false)
   .setCustomId(`delbtn`);
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Params ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/**
 * Sends back a list of buttons to be used
 * @param {Number} size
 * @param {Boolean} autoButtonsDel
 * @returns {MessageButton[]} 
*/
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Portal ////////////////////////////////////////////////////////////////////////////////////////////////////////////////
module.exports = buttonBuilder = async(size, autoButtonsDel) => {
   let buttonList
   if (size <= 3) {
      buttonList = [previous, next];
   } else {
      buttonList = [first, previous, next, last];
   }
   if (autoButtonsDel) buttonList.push(del)
   return buttonList;
}